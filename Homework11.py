# –ü–æ –≥—Ä–∞—Ñ–∏–∫—É –æ–ø—Ä–µ–¥–µ–ª–∏—Ç–µ, –ø—Ä–∏ –∫–∞–∫–∏—Ö –∑–Ω–∞—á–µ–Ω–∏—è x –∑–Ω–∞—á–µ–Ω–∏–µ —Ñ—É–Ω–∫—Ü–∏–∏ –æ—Ç—Ä–∏—Ü–∞—Ç–µ–ª—å–Ω–æ.
def zd1():
    x_list = []
    y_list = []

    for x in range(-10, 11):
        y = 5 * x * x + 10 * x - 30
        x_list.append(x)
        y_list.append(y)


    plt.axhline(y = 0, color = 'r')
    plt.plot(y_list)
    plt.show()    
zd1()
